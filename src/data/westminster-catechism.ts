// Helper functions
export function getCatechismByCategory(category: CatechismCategory): CatechismQuestion[] {
  return WESTMINSTER_CATECHISM.filter(q => q.category === category);
}

export function getCatechismByDifficulty(difficulty: Difficulty): CatechismQuestion[] {
  return WESTMINSTER_CATECHISM.filter(q => q.difficulty === difficulty);
}

export function getRandomCatechism(count: number = 1): CatechismQuestion[] {
  const shuffled = [...WESTMINSTER_CATECHISM].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, WESTMINSTER_CATECHISM.length));
}

export function getCatechismById(id: string): CatechismQuestion | undefined {
  return WESTMINSTER_CATECHISM.find(q => q.id === id);
}

export function getCatechismByNumber(number: number): CatechismQuestion | undefined {
  return WESTMINSTER_CATECHISM.find(q => q.number === number);
}
